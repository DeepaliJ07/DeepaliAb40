//Assignment 2 -Create 4 static method with different name and call it inside the main method.
package Assignments;

public class StaticMethodsCalling_2 
{

	static void methodOne()
	{
		System.out.println("Static method one");
	}
	
	static void methodTwo()
	{
		System.out.println("Static method two");
	}
	
	static void methodThree()
	{
		System.out.println("Static method three");
	}
	static void methodFour()
	{
		System.out.println("Static method four");
	}
	public static void main(String[] args)
	{
		methodOne();
		methodTwo();
		methodThree();
		methodFour();	
	}
}
