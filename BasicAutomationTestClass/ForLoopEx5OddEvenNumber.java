package BasicAutomationTestClass;

public class ForLoopEx5OddEvenNumber
{
public static void main(String[] args)
{
	System.out.println("Odd numbers are");
	for(int i=1;i<=10;i++)
	{
		
		System.out.println(i);
		i++;
	}
	
	System.out.println("Even Numbers are");
	for(int i=0;i<=10;i++)
	{
		System.out.println(i);
		i++;
	}
}
}
